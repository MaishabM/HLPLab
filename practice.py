import sys

class Account:
    """Represents a customer's individual bank account."""
    def __init__(self, account_number, pin, account_holder, initial_balance=0.0):
        self.account_number = account_number
        self.__pin = pin  # Encapsulation: Private variable to secure the PIN
        self.account_holder = account_holder
        self.__balance = initial_balance  # Encapsulation: Protected balance access

    def verify_pin(self, entered_pin):
        """Verifies if the entered PIN matches the account PIN securely."""
        return self.__pin == entered_pin

    def get_balance(self):
        """Getter method to securely check the balance."""
        return self.__balance

    def deposit(self, amount):
        """Adds funds to the account if the amount is valid."""
        if amount > 0:
            self.__balance += amount
            return True, f"Successfully deposited ${amount:.2f}"
        return False, "Invalid deposit amount."

    def withdraw(self, amount):
        """Deducts funds if the balance is sufficient."""
        if amount <= 0:
            return False, "Invalid withdrawal amount."
        if amount > self.__balance:
            return False, "Insufficient funds available."

        self.__balance -= amount
        return True, f"Successfully withdrew ${amount:.2f}"


