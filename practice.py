"""Design a library management system where define a base class LibraryItem and derive classes Book and CD from it. Include appropriate attributes and demonstrate method overriding where applicable. Create instances of the classes and implement a method to display all issued items in the system."""

class LibraryItem:
    def __init__(self, name, year, status):
        self.name = name
        self.year = year
        self.__status = status

    def borrow(self):
        print('The item is issued')

    def ret(self):
        print('The item is returned')

    def display(self):
        print('Name: ',self.name)
        print('Year: ',self.year)

    def get_status(self):
        return self.__status

    def set_status(self,status):
        self.__status = status

class Book(LibraryItem):
    def __init__(self,name,year,status,author):
        super().__init__(name,year,status)
        self.author = author

    def borrow(self):
        print('The book is issued')
        super().set_status('Issued')

    def ret(self):
        print('The book is returned')
        super().set_status('Available')

class CD(LibraryItem):
    def __init__(self,name,year,status,publisher):
        super().__init__(name,year,status)
        self.publisher = publisher

    def borrow(self):
        print('The CD is issued')
        super().set_status('Issued')

    def ret(self):
        print('The CD is returned')
        super().set_status('Available')

